class ForDemo {
    public static void main(String[] args) {
        for (int x=0; x<3; x++) {
            System.out.println("x="+x);
        }

        int y = 0;
        while (y<3) {
            System.out.println("y="+y);
            y++;
        }

    System.out.println("y======="+y);
    }
}